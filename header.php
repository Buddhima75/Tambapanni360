 <script>
     document.addEventListener("DOMContentLoaded", function() {
         const menuItems = document.querySelectorAll('.menu-item-has-children');

         // Close dropdown when clicking anywhere on the document
         document.addEventListener('click', function(event) {
             menuItems.forEach(item => {
                 const subMenu = item.querySelector('.sub-menu');
                 const arrow = item.querySelector('.arrow');
                 if (subMenu && !item.contains(event.target)) {
                     subMenu.classList.remove('show');
                     arrow.style.transform = 'rotate(0deg)';
                 }
             });
         });

         menuItems.forEach(function(menuItem) {
             const subMenu = menuItem.querySelector('.sub-menu');
             const arrow = menuItem.querySelector('.arrow');

             // Handle click on Tours button
             menuItem.addEventListener('click', function(e) {
                 e.preventDefault();
                 e.stopPropagation();

                 // Close all other dropdowns first
                 menuItems.forEach(item => {
                     if (item !== menuItem) {
                         const otherSubMenu = item.querySelector('.sub-menu');
                         const otherArrow = item.querySelector('.arrow');
                         if (otherSubMenu) {
                             otherSubMenu.classList.remove('show');
                             otherArrow.style.transform = 'rotate(0deg)';
                         }
                     }
                 });

                 // Toggle current dropdown
                 if (subMenu.classList.contains('show')) {
                     subMenu.classList.remove('show');
                     arrow.style.transform = 'rotate(0deg)';
                 } else {
                     subMenu.classList.add('show');
                     arrow.style.transform = 'rotate(180deg)';
                 }
             });

             // Handle clicks on submenu links
             const links = subMenu.querySelectorAll('a');
             links.forEach(link => {
                 link.addEventListener('click', function(e) {
                     e.stopPropagation();
                     window.location.href = this.href;
                 });
             });
         });

         // Mobile Menu Toggle
         const menuToggle = document.querySelector('.menu-toggle');
         const navIcon = document.querySelector('.nav-icon');
         const mobileMenu = document.querySelector('.main-menu-mobile-container');

         if (menuToggle && navIcon && mobileMenu) {
             menuToggle.addEventListener('click', function() {
                 navIcon.classList.toggle('open');
                 mobileMenu.classList.toggle('active');
                 document.body.style.overflow = mobileMenu.classList.contains('active') ? 'hidden' : '';
             });
         }
     });
 </script>


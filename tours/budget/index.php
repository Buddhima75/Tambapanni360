        document.addEventListener('DOMContentLoaded', function() {
            let lastScrollTop = 0;
            let ticking = false;
            const parallaxBg = document.querySelector('.parallax-bg');
            const heroSection = document.querySelector('.hero-banner');

            function updateParallax(scrolled) {
                const speed = 0.15;
                const offset = scrolled * speed;
                const opacity = Math.max(0, 1 - (scrolled / (window.innerHeight * 0.5)));

                if (parallaxBg && heroSection.getBoundingClientRect().bottom > 0) {
                    parallaxBg.style.transform = `translate3d(0, ${offset}px, 0) scale(${1 + scrolled * 0.0002})`;
                    document.querySelector('.overlay-gradient').style.opacity = 0.8 * opacity;
                }

                lastScrollTop = scrolled;
                ticking = false;
            }

            window.addEventListener('scroll', function() {
                const scrolled = window.pageYOffset;

                if (!ticking) {
                    window.requestAnimationFrame(function() {
                        updateParallax(scrolled);
                    });
                    ticking = true;
                }
            }, {
                passive: true
            });

            // Smooth scroll on click
            document.querySelector('.scroll-indicator').addEventListener('click', function(e) {
                e.preventDefault();
                const targetPosition = window.innerHeight;
                const startPosition = window.pageYOffset;
                const distance = targetPosition - startPosition;
                const duration = 1000;
                let start = null;

                function animation(currentTime) {
                    if (start === null) start = currentTime;
                    const timeElapsed = currentTime - start;
                    const run = ease(timeElapsed, startPosition, distance, duration);
                    window.scrollTo(0, run);
                    if (timeElapsed < duration) requestAnimationFrame(animation);
                }

                function ease(t, b, c, d) {
                    t /= d / 2;
                    if (t < 1) return c / 2 * t * t + b;
                    t--;
                    return -c / 2 * (t * (t - 2) - 1) + b;
                }

                requestAnimationFrame(animation);
            });
        });
